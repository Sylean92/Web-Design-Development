	function show1(){
		var stats =  document.getElementById("win1").style.display;
		
			if (stats == "none"){  
				document.getElementById("win1").style.display = "inline-block";
				document.getElementById("win1exit").style.display = "inline-block";
			} 
			else{
			
				exitShow1();
				//document.getElementById("win1").style.display = "none";
			}
	}

	function exitShow1() {
		
		var stats2 = document.getElementById("win1exit").style.display;
		
			if (stats2 == "inline-block"){
				document.getElementById("win1").style.display = "none"; 
				document.getElementById("win1exit").style.display = "none";
			}
	
	
	}
	
